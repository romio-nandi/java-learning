package learning;

public class MultiplicationTable {
    public static void main(String[] args) {
        for(int i=1;i<=3;i++){
            for(int j=1;j<=3;j++){
                int k=j*i;
                System.out.print(k);
                System.out.print(" ");
            }
            System.out.println(" ");
        }

    }
}










