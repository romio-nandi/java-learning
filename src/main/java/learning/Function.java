package learning;

import java.lang.Math;


class Function {
    public static void main(String args[]) {
        double x;
        x = Math.max(22, 23);
        System.out.println("Print the maximum number" + x);
        x = Math.min(12, 23);
        System.out.println("Print the minimum number" + x);
        x = Math.sqrt(64);
        System.out.println("Print the square root number" + x);
    }


}





